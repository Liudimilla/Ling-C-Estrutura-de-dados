/*Cálculo Simples
Neste problema, deve-se ler o código de uma peça 1, o número de peças 1, o valor unitário de cada peça 1, 
o código de uma peça 2, o número de peças 2 e o valor unitário de cada peça 2. Após, 
calcule e mostre o valor a ser pago.
Entrada
O arquivo de entrada contém duas linhas de dados. Em cada linha haverá 3 valores, 
respectivamente dois inteiros e um valor com 2 casas decimais.
Saída
A saída deverá ser uma mensagem conforme o exemplo fornecido abaixo, lembrando de 
deixar um espaço após os dois pontos e um espaço após o $. O valor deverá ser 
apresentado com 2 casas após o ponto.
*/
#include<stdio.h>
#include<stdlib.h>
int main ()
{
   int cod_1,num_1,cod_2,num_2;
   float val_u1,val_u2,sub;
   
   scanf("%d %d",&cod_1,&num_1);
   scanf("%f",&val_u1);
   scanf("%d %d",&cod_2,&num_2);
   scanf("%f",&val_u2);
   
   
   
   sub = (num_1*val_u1)+(num_2*val_u2);
   
   printf("VALOR A PAGAR: R$ %.2f\n", sub );

   system("pause");
}
   
     
