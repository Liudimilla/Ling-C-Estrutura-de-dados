/*Leia os quatro valores correspondentes aos eixos x e y de dois pontos quaisquer no plano, p1(x1,y1) e p2(x2,y2) e calcule a distância 
entre eles, mostrando 4 casas decimais após a vírgula, segundo a fórmula:
Entrada
O arquivo de entrada contém duas linhas de dados. A primeira linha contém dois valores de ponto flutuante: 
 x1 y1 e a segunda linha contém dois valores de ponto flutuante x2 y2.
Saída
Calcule e imprima o valor da distância segundo a fórmula fornecida, com 4 casas após o ponto decimal.
*/
#include <stdio.h>
#include <stdlib.h>
#include <math.h>

main ( )
{
     float x1,x2,y1,y2,p1,p2,p3;
     scanf("%f %f",&x1,&y1);
     scanf("%f %f",&x2,&y2);
     p1=((x2-x1)*(x2-x1));
     p2=((y2-y1)*(y2-y1));
     p3=sqrt(p1+p2);
     printf("%.4f\n",p3 );
     
     
     system("pause");
     }
     
     
     
